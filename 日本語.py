class 車の設計図:
 
    def __init__(自分自身, 色='白'):
        自分自身.色 = 色
 
    def 色教えて(自分自身):
        メッセージ = '車の色は{}です'
        print(メッセージ.format(自分自身.色))
 
    def 色変えて(自分自身, 色):
        自分自身.色 = 色
        メッセージ = '車の色を{}に変更しました'
        print(メッセージ.format(自分自身.色))
        
私の車 = 車の設計図()
私の車.色教えて()
私の車.色変えて('黒')
